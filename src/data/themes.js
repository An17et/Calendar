export const getRandomHeroImage = (monthIndex) => {
  const images = themes[monthIndex]?.heroImages;
  if (!images || images.length === 0) return themes[monthIndex]?.heroUrl || '';
  return images[Math.floor(Math.random() * images.length)];
};

